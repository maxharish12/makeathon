async function calculateAIFairPrice(cropKey, category, originalBasePrice) {
    let finalPrice = originalBasePrice;
    
    // Array of distinct Indian States for dynamic varied demoing
    const indianStates = [
        { name: "Maharashtra", lat: 19.0760, lon: 72.8777 },  // Mumbai
        { name: "Tamil Nadu", lat: 13.0827, lon: 80.2707 },   // Chennai
        { name: "Punjab", lat: 31.6340, lon: 74.8723 },       // Amritsar
        { name: "West Bengal", lat: 22.5726, lon: 88.3639 },  // Kolkata
        { name: "Karnataka", lat: 12.9716, lon: 77.5946 },    // Bangalore
        { name: "Rajasthan", lat: 26.9124, lon: 75.7873 },    // Jaipur
        { name: "Kerala", lat: 9.9312, lon: 76.2673 },        // Kochi
        { name: "Gujarat", lat: 23.0225, lon: 72.5714 }       // Ahmedabad
    ];
    
    const randomState = indianStates[Math.floor(Math.random() * indianStates.length)];
    let city = randomState.name;
    let tempC = 28;
    let humidity = 65;
    
    try {
        // Fetch real weather for the randomly selected Indian state
        const weatherReq = await fetch(`https://api.open-meteo.com/v1/forecast?latitude=${randomState.lat}&longitude=${randomState.lon}&current_weather=true&hourly=relative_humidity_2m`);
        const weatherData = await weatherReq.json();
            
        if (weatherData.current_weather) {
            tempC = weatherData.current_weather.temperature;
            if (weatherData.hourly && weatherData.hourly.relative_humidity_2m) {
                humidity = weatherData.hourly.relative_humidity_2m[0];
            }
        }
    } catch(err) {
        console.warn("API Fetch failed, using fallback ML values", err);
    }
    
    let scaler = 1.0;
    let soilTypes = ['Alluvial', 'Black Soil', 'Red Soil', 'Laterite'];
    let soil = soilTypes[city.length % soilTypes.length];
    
    if (tempC > 32) scaler += category === 'fruit' ? 0.35 : 0.20; 
    else if (tempC < 15) scaler += category === 'vegetable' ? 0.15 : 0.05; 
    
    if (humidity > 80) scaler -= 0.10; 
    
    finalPrice = Math.floor(originalBasePrice * scaler);
    
    return {
        price: finalPrice,
        city: city,
        soil: soil,
        temp: tempC,
        hum: humidity
    };
}

