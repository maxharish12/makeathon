function sendFarmerReply() {
    const input = document.getElementById('farmer-input');
    const text = input.value.trim();
    if(!text) return;

    appendUserBubble(text);
    input.value = '';

    const lowerText = text.toLowerCase();

    if (currentBotStep === 0) {
        if (lowerText.includes('fruit') || lowerText.includes('பழம்') || lowerText.includes('ಪ') || lowerText.includes('ഫ') || lowerText.includes('ఫ') || lowerText.includes('फ') || lowerText.includes('ফ')) {
            farmerCategory = 'fruit';
            currentBotStep++;
            setTimeout(() => appendBotBubble('f_2'), 400);
        } else if (lowerText.includes('veg') || lowerText.includes('காய்கறி') || lowerText.includes('ತ') || lowerText.includes('പ') || lowerText.includes('స') || lowerText.includes('स') || lowerText.includes('স')) {
            farmerCategory = 'vegetable';
            currentBotStep++;
            setTimeout(() => appendBotBubble('f_3'), 400);
        } else {
            setTimeout(() => appendBotBubble('f_4'), 400);
        }
    } 
    else if (currentBotStep === 1) {
        let isValid = false;
        if (farmerCategory === 'fruit' && validFruits.some(f => lowerText.includes(f))) isValid = true;
        if (farmerCategory === 'vegetable' && validVegetables.some(v => lowerText.includes(v))) isValid = true;

        if (isValid) {
            selectedCrop = text; 
            currentBotStep++;
            setTimeout(() => appendBotBubble('f_5'), 400);
        } else {
            setTimeout(() => appendBotBubble('err1'), 400);
        }
    }
    else if (currentBotStep === 2) {
        // Extract the numerical value from input (e.g. '120 kg' -> 120)
        const numMatch = text.match(/\d+/);
        let qtyNum = parseInt(numMatch ? numMatch[0] : "0", 10);
        
        if (qtyNum > 1000) {
            setTimeout(() => appendBotBubble('err2'), 400);
            return;
        } else if (qtyNum <= 0) {
            setTimeout(() => appendBotBubble('err3'), 400);
            return;
        }

        selectedQty = text;
        document.getElementById('farmer-input-area').classList.add('hidden');
        currentBotStep++;
        
        setTimeout(() => {
            appendBotBubble('sat'); 
            setTimeout(() => {
                processFarmerResults();
            }, 600);
        }, 400);
    }
}

function startVoiceRecognition() {
    const SpeechRecognition = window.SpeechRecognition || window.webkitSpeechRecognition;
    if (!SpeechRecognition) {
        alert("Voice recognition is not natively supported in your current browser session. Please try Safari or Chrome, or type your answer.");
        return;
    }
    
    const recognition = new SpeechRecognition();
    const langMapVoice = {'en': 'en-IN', 'ta': 'ta-IN', 'hi': 'hi-IN', 'te': 'te-IN', 'mr': 'mr-IN', 'bn': 'bn-IN', 'kn': 'kn-IN', 'ml': 'ml-IN'};
    recognition.lang = langMapVoice[currentLang] || 'en-IN';
    recognition.interimResults = false;
    recognition.maxAlternatives = 1;
    
    const micBtn = document.getElementById('mic-btn');
    micBtn.style.color = '#e74c3c'; 
    micBtn.style.borderColor = '#e74c3c';
    
    recognition.start();
    
    recognition.onresult = function(event) {
        const transcript = event.results[0][0].transcript;
        document.getElementById('farmer-input').value = transcript;
        micBtn.style.color = 'var(--text-muted)';
        micBtn.style.borderColor = 'var(--border-subtle)';
        
        // Let user see what they spoke for 0.5s before hitting send naturally
        setTimeout(() => { sendFarmerReply(); }, 500);
    };
    
    recognition.onerror = function(event) {
        console.error("Speech error:", event.error);
        alert("Microphone error. Please ensure permissions are granted.");
        micBtn.style.color = 'var(--text-muted)';
        micBtn.style.borderColor = 'var(--border-subtle)';
    };
    
    recognition.onend = function() {
        micBtn.style.color = 'var(--text-muted)';
        micBtn.style.borderColor = 'var(--border-subtle)';
    };
}

