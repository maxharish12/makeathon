let currentScanner = null;

function processDecodedQR(decodedText, portalId, expectedBatchId) {
    let parsed = null;
    try { parsed = JSON.parse(decodedText); } catch(e) {}
    
    if (parsed && parsed.bid) {
        // Enforce recent batch ID
        if (parsed.bid !== expectedBatchId) {
            return alert(`❌ Old/Invalid QR: Expected Batch ${expectedBatchId} but scanned ${parsed.bid}. Please scan the most natively recent QR.`);
        }

        // Strict Sequential Validation
        let isValid = false;
        let errMsg = "";
        
        if (portalId === 'broker') {
            if (parsed.f && !parsed.b) isValid = true;
            else errMsg = "Broker interface MUST scan the Farmer's direct QR code.";
        } else if (portalId === 'mandi') {
            if (parsed.b && !parsed.m) isValid = true;
            else errMsg = "Mandi interface MUST scan the official Broker QR code.";
        } else if (portalId === 'retailer') {
            if (parsed.m && !parsed.r) isValid = true;
            else errMsg = "Retailer interface MUST scan the official Mandi QR code.";
        } else if (portalId === 'public') {
            if (parsed.r) isValid = true;
            else errMsg = "Public scanning must use the official Retailer Verified QR code.";
        } else {
            isValid = true;
        }

        if (!isValid) {
            alert(`❌ Invalid QR Sequence: ${errMsg}`);
            return;
        }

        window.agroChainState = parsed;
        
        // Hydrate legacy global variables for UI rendering
        window.currentBasePrice = parsed.f?.p || 18;
        window.currentBrokerTargetPrice = parsed.b?.p || window.currentBasePrice * 1.25;
        window.currentMandiTargetPrice = parsed.m?.p || window.currentBrokerTargetPrice * 1.25;
        window.currentRetailerTargetPrice = parsed.r?.p || window.currentMandiTargetPrice * 1.25;
        window.globalBatchTime = parsed.f?.t || new Date().toLocaleTimeString();
        if (parsed.f?.n) window.currentFarmerName = parsed.f.n;
        if (parsed.b?.n) window.currentBrokerName = parsed.b.n;
        if (parsed.m?.n) window.currentMandiName = parsed.m.n;
        if (parsed.r?.n) window.currentRetailerName = parsed.r.n;
        if (parsed.crp) selectedCrop = parsed.crp;
        if (parsed.qty) selectedQty = parsed.qty;
        if (parsed.ml) window.globalMLContext = parsed.ml;
        
        const dynamicBatchEl = document.getElementById('dynamic-batch-id');
        if (dynamicBatchEl) dynamicBatchEl.innerText = parsed.bid;
        
        processScanSuccess(portalId);
    } else if (decodedText === expectedBatchId) {
        // Fallback to old simple string ID system
        processScanSuccess(portalId);
    } else {
        if (window.lastFailedScan !== decodedText) {
            alert(`❌ Invalid QR Detected: "${decodedText}".`);
            window.lastFailedScan = decodedText;
        }
    }
}

function handleFileScan(event, portalId) {
    const file = event.target.files[0];
    if (!file) return;

    const dynamicBatchEl = document.getElementById('dynamic-batch-id');
    const expectedBatchId = dynamicBatchEl ? dynamicBatchEl.innerText.trim() : 'AGRO-OD-4928';

    const html5QrCode = new Html5Qrcode(`reader-${portalId}`);
    html5QrCode.scanFile(file, true)
    .then(decodedText => {
        processDecodedQR(decodedText, portalId, expectedBatchId);
    })
    .catch(err => {
        console.error('File scan error', err);
        alert('Could not find a valid QR payload in that image. Try capturing the QR clearly.');
    });
}

function triggerScan(portalId) {
    const scanZone = document.getElementById(portalId + '-scan');
    scanZone.classList.add('scanning');

    const readerId = `reader-${portalId}`;
    const dynamicBatchEl = document.getElementById('dynamic-batch-id');
    const expectedBatchId = dynamicBatchEl ? dynamicBatchEl.innerText.trim() : 'AGRO-OD-4928';

    const icon = document.getElementById(`icon-${portalId}`);
    const sweep = document.getElementById(`sweep-${portalId}`);
    if (icon) icon.style.display = 'none';
    if (sweep) sweep.style.display = 'none';

    try {
        if (currentScanner) {
            try { currentScanner.stop().catch(e=>{}); } catch(e){}
        }
        // Initialize Html5Qrcode
        currentScanner = new Html5Qrcode(readerId);

        currentScanner.start(
            { facingMode: "environment" },
            { fps: 5, qrbox: { width: 150, height: 150 } },
            (decodedText, decodedResult) => {
                processDecodedQR(decodedText, portalId, expectedBatchId);
            },
            (errorMessage) => { /* ignore generic unfocused errors */ }
        ).catch((err) => {
            console.error("Camera fail:", err);
            alert("Camera access denied or run safely required via HTTPS. Loading Simulation fallback...");
            simulateQRScanFallback(portalId);
        });
    } catch (e) {
        console.error("Scanner init failed", e);
        alert("Camera initialization failed. Fallback simulation triggered.");
        simulateQRScanFallback(portalId);
    }
}

