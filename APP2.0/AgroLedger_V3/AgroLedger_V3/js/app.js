function acceptRetailer() {
    const val = parseFloat(document.getElementById('retailer-price-input').value);
    const landedCost = window.currentMandiTargetPrice || 28; 
    const maxAllowed = landedCost * 1.25;
    
    if(isNaN(val) || val <= 0) return alert('Enter a valid retail price.');
    if(val > maxAllowed) return alert(i18n[currentLang]['alert_limit'] + ` (₹${maxAllowed.toFixed(1)})`);
    
    window.currentRetailerTargetPrice = val;
    
    // Accumulate State & Generate Retailer QR
    if(!window.agroChainState) window.agroChainState = {};
    window.agroChainState.r = { n: window.currentRetailerName || 'FreshMart', p: val, t: new Date().toLocaleTimeString([], {hour: '2-digit', minute:'2-digit'}) };

    showRetailerSuccess();
    
    if(window.portalStates) {
        window.portalStates.retailer = 'success';
        StorageDB.save();
    }
}

