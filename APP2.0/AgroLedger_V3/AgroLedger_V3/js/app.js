function handlePublicPhotoCapture() {
    const fileInput = document.getElementById('public-camera-input');
    const textEl = document.getElementById('public-camera-text');
    const iconEl = document.getElementById('public-camera-icon');
    
    if (fileInput && fileInput.files && fileInput.files.length > 0) {
        textEl.innerText = "Photo Captured";
        textEl.style.color = "var(--green)";
        if(iconEl) {
            iconEl.setAttribute('name', 'checkmark-circle');
            iconEl.style.color = "var(--green)";
        }
        
        const file = fileInput.files[0];
        const reader = new FileReader();
        reader.onload = function(e) {
            let previewImg = document.getElementById('public-photo-preview');
            if(!previewImg) {
                previewImg = document.createElement('img');
                previewImg.id = 'public-photo-preview';
                previewImg.style.width = '100%';
                previewImg.style.marginTop = '10px';
                previewImg.style.borderRadius = '8px';
                previewImg.style.objectFit = 'cover';
                previewImg.style.maxHeight = '200px';
                fileInput.parentNode.appendChild(previewImg);
            }
            previewImg.src = e.target.result;
        }
        reader.readAsDataURL(file);
    }
}

